#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	num = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (-2147483648 < n && n <= 2147483647)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n / 10 != 0)
		{
			ft_putnbr_fd(n / 10, fd);
		}
		num = n % 10 + '0';
		write(fd, &num, 1);
	}
}
